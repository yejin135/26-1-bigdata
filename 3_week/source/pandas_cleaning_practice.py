import pandas as pd

# ──────────────────────────────────────
# 현실에서 마주치는 "지저분한" 데이터
# ──────────────────────────────────────
messy_data = {
    "상품명": ["노트북", "마우스 ", " 키보드", "모니터", "노트북", "마우스 ", "스피커"],
    "가격": ["1,200,000", "35,000", "미입력", "450,000", "1,200,000", "35,000", "55,000"],
    "도시": ["서울", "서울시", " 부산 ", "서울특별시", "서울", "서울시", "부산광역시"],
    "판매일": ["2026-01-15", "2026/02/20", "2026.03.08", "26-01-22", "2026-01-15", "2026/02/20", "2026-03-01"],
    "재고": ["100", "250", "80", "None", "100", "250", "30"]
}
df = pd.DataFrame(messy_data)

print("=" * 60)
print("원본 데이터 (지저분한 상태)")
print("=" * 60)
print(df)
print(f"\n데이터 타입:\n{df.dtypes}")

# ──────────────────────────────────────
# Step 1: 문자열 정제 — strip() → replace()
# ──────────────────────────────────────
print("\n" + "=" * 60)
print("Step 1: 문자열 정제")
print("=" * 60)

# 상품명 양쪽 공백 제거
df["상품명"] = df["상품명"].str.strip()

# 도시명 통일: strip() 먼저, 그다음 replace()
df["도시"] = df["도시"].str.strip()
df["도시"] = df["도시"].replace({
    "서울시": "서울",
    "서울특별시": "서울",
    "부산광역시": "부산"
})

print("정제 후 도시 고유값:", df["도시"].unique())
print("정제 후 상품명 고유값:", df["상품명"].unique())

# ──────────────────────────────────────
# Step 2: 타입 변환 — 문자열 → 숫자/날짜
# ──────────────────────────────────────
print("\n" + "=" * 60)
print("Step 2: 타입 변환")
print("=" * 60)

# 가격: 쉼표 제거 후 숫자로 변환 (변환 불가한 "미입력"은 NaN)
df["가격"] = pd.to_numeric(df["가격"].str.replace(",", ""), errors="coerce")
print(f"가격 타입: {df['가격'].dtype}")
print(f"가격 결측치: {df['가격'].isnull().sum()}개")

# 재고: "None" 문자열을 NaN으로 처리 후 숫자 변환
df["재고"] = pd.to_numeric(df["재고"].replace("None", None), errors="coerce")

# 날짜: 다양한 형식을 자동 인식하여 datetime으로 변환
df["판매일"] = pd.to_datetime(df["판매일"], format="mixed")
print(f"판매일 타입: {df['판매일'].dtype}")

# ──────────────────────────────────────
# Step 3: 결측치 처리
# ──────────────────────────────────────
print("\n" + "=" * 60)
print("Step 3: 결측치 처리")
print("=" * 60)

print(f"결측치 현황:\n{df.isnull().sum()}")
df["가격"] = df["가격"].fillna(df["가격"].median())
df["재고"] = df["재고"].fillna(0).astype(int)
print(f"\n처리 후 결측치:\n{df.isnull().sum()}")

# ──────────────────────────────────────
# Step 4: 중복 제거
# ──────────────────────────────────────
print("\n" + "=" * 60)
print("Step 4: 중복 제거")
print("=" * 60)

print(f"중복 행: {df.duplicated().sum()}개")
print("중복 행 내용:")
print(df[df.duplicated(keep=False)])

df = df.drop_duplicates().reset_index(drop=True)
print(f"\n중복 제거 후: {len(df)}행")

# ──────────────────────────────────────
# 최종 결과 확인
# ──────────────────────────────────────
print("\n" + "=" * 60)
print("최종 정제 데이터")
print("=" * 60)
print(df)
print(f"\n데이터 타입:\n{df.dtypes}")